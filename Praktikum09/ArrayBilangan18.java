public class ArrayBilangan18 {

    public static void main(String[] args) {
        int[] bil = new int[4];

        bil [0] = 5;
        bil [1] = 128667;
        bil [2] = 7;
        bil [3] = 2000000;

        for (int i = 0; i <= 4; i++) {
            System.out.println(bil[i]);
        }
    }
}